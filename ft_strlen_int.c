#include "printf.h"

int	ft_strlen_int(int nbr)
{
	int	i;

	i = 0;
	if (nbr == 0)
		return (i + 1);
	while (nbr != 0)
	{
		i++;
		nbr = nbr / 10;
	}
	return (i);
}
