#include "printf.h"

void	ft_putnbr(int n)
{
	char	*nbr;

	nbr = "0123456789";
	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			n = n * -1;
			write (1, "-", 1);
		}
		if (n > 9)
			ft_putnbr(n / 10);
		ft_putchar((n % 10 + '0'));
	}
}
